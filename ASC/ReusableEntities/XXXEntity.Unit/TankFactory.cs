        internal static string Get_QmtIdx_TmuDi(DateTime oDate, string Mode, string deviceID)
        {
            string QmtIndex = "";
            switch (Mode)
            {
                case "Hour":
                    QmtIndex = "Hour" + oDate.ToString("HH") + "_" + deviceID;
                    break;
                case "Day":
                    QmtIndex = "Day" + oDate.ToString("dd") + "_" + deviceID;
                    break;
                case "Week":
                    DayOfWeek day = CultureInfo.InvariantCulture.Calendar.GetDayOfWeek(oDate);
                    int result = (int)day;
                    QmtIndex = "Week" + result.ToString("00") + "_" + deviceID;
                    break;
                case "Month":
                    QmtIndex = "Month" + oDate.ToString("MM") + "_" + deviceID;
                    break;
                default:
                    Console.WriteLine("Default case");
                    break;
            }
            return QmtIndex;
        }

