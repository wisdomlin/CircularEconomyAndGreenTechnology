        public static List<string> GetSpatialModeList()
        {
            List<string> TankIdList = new List<string>();
            // 年均值
            for (int i = 2018; i <= 2020; i++)
            {
                string key = "Y" + i.ToString("0000");
                TankIdList.Add(key);
            }
            // 季均值
            for (int i = 2018; i <= 2020; i++)
            {
                for (int j = 1; j <= 4; j++)
                {
                    string key = "Y" + i.ToString("0000") + "S" + j.ToString("00");
                    TankIdList.Add(key);
                }
            }
            return TankIdList;
        }

